create table
    public.variants (
                        id bigint generated by default as identity not null,
                        created_at timestamp with time zone not null default now(),
                        name text null,
                        product_id bigint null,
                        constraint variants_pkey primary key (id),
                        constraint variants_id_key unique (id),
                        constraint variants_product_id_fkey foreign key (product_id) references products (id) on update cascade on delete cascade
) tablespace pg_default;
CREATE INDEX IF NOT EXISTS idx_variants_product_id ON public.variants (product_id) TABLESPACE pg_default;